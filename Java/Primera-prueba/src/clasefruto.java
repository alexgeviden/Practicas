public class clasefruto {
    int id;
    String nombre;
    String descripcion;

    public clasefruto(int id, String nombre, String descripcion) {
        this.id = id;
        this.nombre = nombre;
        this.descripcion = descripcion;
    }
    public int getId() {
        return id;}

    public String getNombre() {

        return nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }
}
